# Defining integers
a = 10
b = 20

# Division of integers
result = b/a
print("Division result: ", result)

# Modulus operation (reminder)
reminder = b%a
print("Modulus (reminder): ", reminder)

# Absolute value
result1 = abs(-7)
print("Absolute Value: ", result1)


# Defining float
c = 3.0
d = 5.0

# addition
print("Adding floats: ", c + d)

# substraction
print("Substracting floats: ", c - d)

# rounding
pi = 3.142857142857143
print("Round by 2 decimal places for PI: ", round(pi, 3))

